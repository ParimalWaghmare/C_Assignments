#include<stdio.h>
#include<limits.h>
int main()
{
	printf("Range of char  c= %d to =%d\n",SCHAR_MIN,SCHAR_MAX );
	printf("Range of unsigned char c =%d \n ",UCHAR_MAX);
	printf("Range of short int =%hd to =%hd\n",SHRT_MIN,SHRT_MAX);
	printf("Range of unsigned short int =%hu\n",USHRT_MAX);
	printf("Range of signed int =%d to =%d\n",INT_MIN,INT_MAX);
	printf("Range of unsigned int =%u\n",UINT_MAX);
	printf("Range of long int= %ld to =%ld\n",LONG_MIN,LONG_MAX );
	printf("Range of unsigned long int  =%lu\n",ULONG_MAX);


	return 0;


}
